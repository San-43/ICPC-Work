void solve() {
    double c;
    cin >> c;


    auto ok = [&](double x) {
        double tmp = x*x + sqrt(x);
        return tmp >= c;
    };

    double l = 0.0, r = 1e6;
    for(int i = 0; i < 60; i++) {
        double mid = l + (r - l) / 2;
        if(ok(mid)) r = mid;
        else l = mid;
    }
    cout << setprecision(20) << l << edl;
}

